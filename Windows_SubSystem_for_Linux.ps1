param ( [switch] $Enable,
        [switch] $Disable
      )
IF ($Enable) {Enable-WindowsOptionalFeature -Online -FeatureName "Microsoft-Windows-Subsystem-Linux" -All -NoRestart; Enable-WindowsOptionalFeature -Online -FeatureName "VirtualMachinePlatform" -All -NoRestart}
IF ($Disable) {Disable-WindowsOptionalFeature -Online -FeatureName "Microsoft-Windows-Subsystem-Linux" -NoRestart; Disable-WindowsOptionalFeature -Online -FeatureName "VirtualMachinePlatform" -NoRestart}
